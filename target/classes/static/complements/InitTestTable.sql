  ID NUMBER Generated ALWAYS AS IDENTITY ( START WITH 1 MAXVALUE 9999 MINVALUE 1 CYCLE CACHE 20) NOT NULL,
  NOMBRE VARCHAR2(1024) NOT NULL,
  EDAD INTEGER NOT NULL,
  DIRECCION VARCHAR2(1024) NOT NULL
);

ALTER TABLE PERSONA ADD CONSTRAINT ENTIDAD_PK PRIMARY KEY (ID);


